package com.pwioi.composite_entities;

public class Brain {
	private String colour;
	private int brainWeight;
	public Brain(String colour, int brainWeight) {
		super();
		this.colour = colour;
		this.brainWeight = brainWeight;
	}
	public String getColour() {
		return colour;
	}
	public int getBrainWeight() {
		return brainWeight;
	}
	

}
